# Modules

#   Modules are python files (.py) that implement a set of functions.
#   Modules are imported using the "import" call.
#   Easy way to store and organise FXNS, classes and their variables for later,
#   or repeated use.

import math_operations

#   Imports the ".py" module that is in the SAME file directory.
print(math_operations.add(5, 2))
#   Each command NEEDS to include "MODULE_NAME.FXN(parameters)"
print(math_operations.subtract(5, 2))
