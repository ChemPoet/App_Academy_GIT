#   Numeric Data

#   Integer = Whole number

num1 = 3
print(3)            #   Don't need (" ") when printing numbers as NUMBERS instead of text.
print(num1)
print(type(num1))   #   Prints what type of numeric data the "num" variable is.
print()

#   Float   = Decimal / Fraction

num2 = 3.14
print(num2)
print(type(num2))   #   Python automatically assigns data types as strings (" ", ' '), integers (1, 78, etc.,),
                    #   or floats (1.0, 3.698741, 0.00001, etc.), without EACH value needing to be declared (as in e.g., C++).